package com.db1.db1start;

// Método estático que some dois números
public class Inteiro {
	public static int somaDois(int num1, int num2) {
			int resultado = num1 + num2;
			return resultado;
	}
		
	public static int subtraiDois(int num1, int num2) {
		int resultado = num1 - num2;
		return resultado;	
	
	}

}
